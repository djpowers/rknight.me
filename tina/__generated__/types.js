export const LinkDocument = gql`
    query link($relativePath: String!) {
  link(relativePath: $relativePath) {
    ... on Document {
      _sys {
        filename
        basename
        breadcrumbs
        path
        relativePath
        extension
      }
      id
    }
    ...LinkParts
  }
}
    ${LinkPartsFragmentDoc}`;
